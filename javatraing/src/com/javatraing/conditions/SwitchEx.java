package com.javatraing.conditions;



public class SwitchEx {
public static void main(String[] args) {

	int num =30;

	switch(num) {
	case 10:
		System.out.println("10");
		break;
	case 20:
		System.out.println("20");
		break;
	case 30:
		System.out.println("30");
		break;
	case 40:
		System.out.println("40");
		break;

	}
}
}
